while True:
  f = float(input("Enter a degree in Fahrenheit, and I'll convert it into Celsius and Kelvin "))
  c = (f - 32) * 5/9
  k = c + 273.15
  print(c, "degrees celsius")
  print(k, "degrees Kelvin")




